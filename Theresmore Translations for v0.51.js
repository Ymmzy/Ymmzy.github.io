var regex_production = new RegExp(/(.+) (research|gold|food|wood|stone|copper|iron|tools|cow|horse|materials|steel|crystal|supplies|saltpetre|natronite|faith|mana)$/);
var regex_per_second = new RegExp(/(.+)\/s$/);
var regex_army = new RegExp(/(.+) (attack|defense)$/);
var regex_army_description = new RegExp(/(.+) \((Ranged|Tank|Shock|Cavalry)\)$/);
var regex_ancestor = new RegExp(/^(Ancestor) (.+)/);
var regex_warning = new RegExp(/^(Warning) \((.+)/);
var regex_difficulty = new RegExp(/^(Difficulty) (.+)/);
var regex_explore = new RegExp(/^(Your scout returned to the settlement and brought back everything he could find)\: ([\d\w, ]+)$/);
var regex_explore_empty = new RegExp(/^(Your scout explored the territories for days but came back empty handed)( \(x\d+\))$/);
var regex_battle = new RegExp(/(.+)\: (Victory\!|You have been defeated\!)$/);
var regex_battle_killed = new RegExp(/^(You killed)\: ([\d\w, ]+)$/);
var regex_battle_died = new RegExp(/^(Died)\: ([\d\w, ]+)$/);
var regex_battle_all = new RegExp(/^(Victory\!|You have been defeated\!) (Died\: [\d\w, ]+)?(. )?(You killed\: [\d\w, ]+)?$/);
var regex_plural = new RegExp(/^(\d+) ([\w ]+)$/);
var regex_plural_list = new RegExp(/(\d+ [\w ]+)/g);

var t = function (originalStr) {
    var tmp = originalStr.trim();
    if (translations[tmp]) {
        return translations[tmp];
    }
    var m = null;
    if (m = originalStr.match(regex_chat)) {
        return m[1] + t(m[2]);
    }
    if (m = originalStr.match(regex_diplomacy)) {
        return trans_from_dict(m[1]) + m[2];
    }
    if (m = originalStr.match(regex_cap)) {
        return trans_from_dict(m[1]) + "上限";
    }
    if (m = originalStr.match(regex_production)) {
        return trans_from_dict(m[1]) + "-" + trans_from_dict(m[2]);
    }
    if (m = originalStr.match(regex_per_second)) {
        return m[1] + "/秒";
    }
    if (m = originalStr.match(regex_army)) {
        return trans_from_dict(m[1]) + trans_from_dict(m[2]);
    }
    if (m = originalStr.match(regex_army_description)) {
        return trans_from_dict(m[1]) + " (" + trans_from_dict(m[2]) + ")";
    }
    if (m = originalStr.match(regex_ancestor)) {
        return trans_from_dict(m[1]) + " " + trans_from_dict(m[2]);
    }
    if (m = originalStr.match(regex_warning)) {
        return trans_from_dict(m[1]) + " (" + trans_from_dict(m[2]);
    }
    if (m = originalStr.match(regex_difficulty)) {
        return trans_from_dict(m[1]) + "-" + trans_from_dict(m[2]);
    }
    if (m = originalStr.match(regex_explore)) {
        return trans_from_dict(m[1]) + "：" + t(m[2]);
    }
    if (m = originalStr.match(regex_explore_empty)) {
        return trans_from_dict(m[1]) + m[2];
    }
    if (m = originalStr.match(regex_battle)) {
        return trans_from_dict(m[1]) + "：" + trans_from_dict(m[2]);
    }
    if (m = originalStr.match(regex_battle_killed)) {
        return trans_from_dict(m[1]) + "：" + t(m[2]);
    }
    if (m = originalStr.match(regex_battle_died)) {
        return trans_from_dict(m[1]) + "：" + t(m[2]);
    }
    if (m = originalStr.match(regex_battle_all)) {
        return trans_from_dict(m[1]) + t(m[2]) + trans_from_dict(m[3]) + t(m[4]);
    }
    if (m = originalStr.match(regex_plural)) {
        return m[1] + trans_from_dict(m[2]);
    }
    if (m = originalStr.match(regex_plural_list)) {
        var str = "";
        var i = 0;
        for ( ; i < m.length - 1; i++) {
            str += t(m[i]) + "，";
        }
        str += t(m[i]);
        return str;
    }
    return originalStr;
}

function translateElement(element) {
    if (element.tagName == 'SCRIPT') return;
    var elTW = document.createTreeWalker(element, NodeFilter.SHOW_Element, null, false);
    var node = null;
    var translate = t;
    while ((node = elTW.nextNode()) != null) {
        if (node.nodeType == 3) {
            if (node.tagName == 'SCRIPT') continue;
            //if (node.nodeValue.length > 200) continue;
            var value = node.nodeValue;
            if (value.match(regex_chat) ||
                value.match(regex_difficulty) ||
                value == "Cast this spell" || value == "Cancel this spell" ||
                value == "The game has been saved to the clipboard" || value == "The game has been saved to file" ||
                value == "Click here to paste a save" || value == "Import pasted text" ||
                value == "Improve relationships" || value == "Alliance" || value == "Insult" || value == "War" ||
                value == "Accept trade agreement" || value == "Cancel trade agreement" ||
                0 ) {
                //聊天框&难度&咒语切换&存档&读档&外交&贸易
                var observer = new MutationObserver((ml, ob) => {
                    ob.disconnect();
                    ml[0].target.nodeValue = translate(ml[0].target.nodeValue);
                    ob.observe(ml[0].target, { characterData: true });
                });
                observer.observe(node, { characterData: true });
            }
            node.nodeValue = translate(value);
        }
    }
}

var text_attack_radio = null;

(function () {
    'use strict';
    QQ(document).on('DOMNodeInserted', (e) => {
        translateElement(e.target);
    });
    QQ(document).on('click', "div > div > div > div > div:nth-child(1) > div > div.flex.flex-wrap > button.btn.btn-dark.w-full.mb-4", (e) => {
        //攻击敌人选框
        text_attack_radio = e.target.childNodes[0];
    });
    QQ(document).on('DOMNodeInserted', "#headlessui-portal-root > div", (e) => {
        //攻击敌人弹窗
        if (text_attack_radio) {
            var observer = new MutationObserver((ml, ob) => {
                text_attack_radio.nodeValue = trans_from_dict(text_attack_radio.nodeValue);
            });
            observer.observe(e.target.childNodes[1].childNodes[0].childNodes[0], { attributes: true });
        }
    });
})();